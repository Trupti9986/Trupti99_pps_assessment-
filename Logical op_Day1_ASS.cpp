#include <iostream>
using namespace std;
 
 int main() {
   int a = 10, b = 5, c = 15;

   // Logical AND
     cout << "Logical AND (a > b && b < c): " << (a > b && b < c)<< endl;

   // Logical OR
    cout << "Logical OR (a < b || b < c): " << (a < b || b < c)<< endl;

   // Logical NOT
    cout << "Logical NOT (!(a > b)): " << !(a > b) << endl;

return 0;

   
 }

